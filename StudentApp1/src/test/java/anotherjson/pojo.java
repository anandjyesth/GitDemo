package anotherjson;

public class pojo {
	
	private popup PopUp;
	private String id;
	private String value;
	
	
	
	public popup getPopUp() {
		return PopUp;
	}
	public void setPopUp(popup popUp) {
		PopUp = popUp;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
	

}
